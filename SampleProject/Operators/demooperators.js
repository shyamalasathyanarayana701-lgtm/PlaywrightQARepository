/*
  I. Arithmetic Operators:
    +       -       Addition
    -       -       subtraction
    *       -       multiplication
    /       -       Division
    %       -       Modulus
*/
 
//Addition
let x,y
x=10;y=40
let result=(x + y)
console.log("Addition Result :"+result);
 
//substraction
let result2=(y-x)
console.log("substraction Result :"+result2);
 
//multiplication
let result3=(x * y)
console.log("Multiplication Result :"+result3);
 
//division
let result4=(y/x)
console.log("Division Result :"+result4);
 
//Modulus
let a,b
